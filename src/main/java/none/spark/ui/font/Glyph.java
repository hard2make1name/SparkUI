package none.spark.ui.font;

public class Glyph {
    public final int width;
    public final int height;
    public final int size;
    public final int textureId;

    public Glyph(int width, int height, int size, int textureId) {
        this.width = width;
        this.height = height;
        this.size = size;
        this.textureId = textureId;
    }
}
